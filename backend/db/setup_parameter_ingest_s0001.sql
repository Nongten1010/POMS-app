WITH column_counts AS (
    SELECT object_id, COUNT(*) AS column_count
    FROM sys.columns
    GROUP BY object_id
),
row_counts AS (
    SELECT object_id, SUM(rows) AS row_count
    FROM sys.partitions
    WHERE index_id IN (0, 1)
    GROUP BY object_id
)
SELECT
    s.name AS schema_name,
    t.name AS table_name,
    cc.column_count,
    COALESCE(rc.row_count, 0) AS row_count
FROM sys.tables t
JOIN sys.schemas s ON t.schema_id = s.schema_id
JOIN column_counts cc ON t.object_id = cc.object_id
LEFT JOIN row_counts rc ON t.object_id = rc.object_id
WHERE s.name = N'ingest'
ORDER BY s.name, t.name;
GO
